//ASCII to character

#include<stdio.h>
int main()
{
    int n;
    printf("Enter an ASCII value =");
    scanf("%d",&n);

    printf("The ASCII character value is = %c",n);
    return 0;

//character to ASCII


    char ch;
    printf("Enter a character value = ");
    scanf("%c",&ch);

    printf("The character ASCII value is =%d",ch);
    return 0;
}
